	private String yeYear;
	private String brName;
	private int thPrice;
	public int getThId() {
		return thId;
	}
	public void setThId(int thId) {
		this.thId = thId;
	}
	public String getYeYear() {
		return yeYear;
	}
	public void setYeYear(String yeYear) {
		this.yeYear = yeYear;
	}
