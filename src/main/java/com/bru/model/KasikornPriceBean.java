package com.bru.model;

public class KasikornPriceBean {

	private int kaId;
	private String yeYear;
	private String brName;
	private int kaPrice;
	public int getKaId() {
		return kaId;
	}
	public void setKaId(int kaId) {
		this.kaId = kaId;
	}
	public String getYeYear() {
		return yeYear;
	}
	public void setYeYear(String yeYear) {
		this.yeYear = yeYear;
	}
	public String getBrName() {
		return brName;
	}
	public void setBrName(String brName) {
		this.brName = brName;
	}
	public int getKaPrice() {
		return kaPrice;
	}
	public void setKaPrice(int kaPrice) {
		this.kaPrice = kaPrice;
	}
	
	
}
