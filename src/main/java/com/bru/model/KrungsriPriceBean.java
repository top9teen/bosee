package com.bru.model;

public class KrungsriPriceBean {

	
	private int krId;
	private String yeYear;
	private String brName;
	private int krPrice;
	public int getKrId() {
		return krId;
	}
	public void setKrId(int krId) {
		this.krId = krId;
	}
	public String getYeYear() {
		return yeYear;
	}
	public void setYeYear(String yeYear) {
		this.yeYear = yeYear;
	}
	public String getBrName() {
		return brName;
	}
	public void setBrName(String brName) {
		this.brName = brName;
	}
	public int getKrPrice() {
		return krPrice;
	}
	public void setKrPrice(int krPrice) {
		this.krPrice = krPrice;
	}
	
	
}
